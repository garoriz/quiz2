package ru.kpfu.itis.garipov.bus_location_app.model;

public class Data {
    public String GaragNumb;
    public String Marsh;
    public String Graph;
    public String Smena;
    public String TimeNav;
    public String Latitude;
    public String Longitude;
    public String Speed;
    public String Azimuth;

    public Data() {
    }

    public String getGaragNumb() {
        return GaragNumb;
    }

    public void setGaragNumb(String garagNumb) {
        GaragNumb = garagNumb;
    }

    public String getMarsh() {
        return Marsh;
    }

    public void setMarsh(String marsh) {
        Marsh = marsh;
    }

    public String getGraph() {
        return Graph;
    }

    public void setGraph(String graph) {
        Graph = graph;
    }

    public String getSmena() {
        return Smena;
    }

    public void setSmena(String smena) {
        Smena = smena;
    }

    public String getTimeNav() {
        return TimeNav;
    }

    public void setTimeNav(String timeNav) {
        TimeNav = timeNav;
    }

    public String getLatitude() {
        return Latitude;
    }

    public void setLatitude(String latitude) {
        Latitude = latitude;
    }

    public String getLongitude() {
        return Longitude;
    }

    public void setLongitude(String longitude) {
        Longitude = longitude;
    }

    public String getSpeed() {
        return Speed;
    }

    public void setSpeed(String speed) {
        Speed = speed;
    }

    public String getAzimuth() {
        return Azimuth;
    }

    public void setAzimuth(String azimuth) {
        Azimuth = azimuth;
    }
}
